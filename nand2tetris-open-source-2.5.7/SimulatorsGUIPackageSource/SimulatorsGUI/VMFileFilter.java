package SimulatorsGUI;

import java.io.File;
import javax.swing.filechooser.FileFilter;

/**
 * A file filter of '.vm' files or directories, for the use of the ProgramComponent.
 */
public class VMFileFilter extends FileFilter {

    /**
     * Determines which files this filter should accept.
     */
    public boolean accept(File f) {
        if (f.isDirectory()) {
            return true;
        }

        String extension = getExtension(f);
        return (extension != null) && (extension.equals("vm"));
    }

    /*
     * Get the extension of a file.
     */
    public static String getExtension(File f) {
        String ext = null;
        String s = f.getName();
        int i = s.lastIndexOf('.');

        if (i > 0 &&  i < s.length() - 1) {
            ext = s.substring(i+1).toLowerCase();
        }
        return ext;
    }

    /**
     * The description of this filter
     */
    public String getDescription() {
        return "VM Files / Dirs";
    }
}
